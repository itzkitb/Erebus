namespace Erebus.Core;

/// <summary>
/// Represents the theme setting for the application
/// </summary>
public enum AppTheme
{
    /// <summary>
    /// Use system theme setting
    /// </summary>
    System = 0,

    /// <summary>
    /// Always use dark theme
    /// </summary>
    Dark = 1,

    /// <summary>
    /// Always use light theme
    /// </summary>
    Light = 2
}
